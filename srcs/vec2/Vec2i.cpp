#include <vec2/Vec2i.hpp>

//**** STATIC FUNCTIONS DEFINE *************************************************

//**** INITIALISION ************************************************************
//---- Constructors ------------------------------------------------------------

Vec2i::Vec2i(void)
{
}


Vec2i::Vec2i(const Vec2i &obj)
{
	this->x = obj.x;
	this->y = obj.y;
}


Vec2i::Vec2i(int x, int y)
{
	this->x = x;
	this->y = y;
}

//---- Destructor --------------------------------------------------------------

Vec2i::~Vec2i()
{
}

